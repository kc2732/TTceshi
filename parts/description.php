        <legend>Q:什么是运动处方</legend>
        运动处方的概念最早是美国生理学家卡波维奇在20世纪50年代提出的。20世纪60年代以来，随着康复医学的发展以及对冠心病等的康复训练的开展，运动处方开始受到重视。1969年世界卫生组织开始使用运动处方术语，从而在国际上得到认可。 运动处方的完整概念是：康复医师或体疗师，对从事体育锻炼者或者病人，根据医学检查资料（包括运动试验和体力测验），按其健康，体力以及心血管功能状况，用处方的形式规定运动种类，运动强度，运动时间及运动频率，</br> 提出运动中的注意事项。运动处方是指导人们有目的，有计划和科学地锻炼的一种方法。
    </fieldset>
