        <legend>Q:糖尿病运动营养处方和普通健身运动营养有什么区别？</legend>
        A:诚然运动营养处方在健身中也有运用。这是一种体育医学和康复医学在大众中的运用，让我们的很多人因此受益。但是我也看到，国内有很多健身教练仍然没有使用运动营养处方的办法，没有计划，没有测评，根据以往个人的经验来给一个与自己完全不同的人开一套锻炼和饮食的办法。</br>
         
        医疗领域的运动营养处方和健身领域的运动营养区别有以下几点：
         </br>
        1）人群不同：健身面对的人群，基本都是相对健康的人，而医疗领域面对的却是有各种需求和讲究的病人。不论是身体心理，病人和正常人都有这非常大的不同。
         </br>
        2）风险不同：健身的大部分群体以相对健康的年轻人为主。这类群体，即使突然开始运动，发生意外，比如心脑血管的意外的风险较小。糖尿病患者,面临很多很麻烦的并发症，比如糖尿病足：糖尿病患者的下肢的伤口很难愈合，缺乏足够的糖尿病相关处理知识，没有个性化的运动，随便模仿会产生非常严重的后果。难以愈合的伤口会最终引发截肢的可能。
         </br>
        3）目的、作用不同；医疗领域，对于糖尿病患者运动和营养处方是应该有治疗作用的，也就是能：1.帮助降低血糖；2.降低药量；3.维持血糖平稳。健身领域，普通人或者运动员通过有计划的运动营养帮助他们达到更好的训练效果，如增肌减脂。
         </br>
        4）使用的人不同：医疗领域的康复处方需要具有相应处方资格的医生或者治疗师开具。健身运动营养处方一般由有资质的教练开具。  
