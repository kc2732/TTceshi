    <p>美国糖尿病联盟的展台聚集了非常多的人。展览的内容却很让人意想不到：我们没有看到最新药物和技术的宣传，整个展厅最多的主题就是两个：运动和教育。</p>
    <br>
    <p>进去展厅就有工作人员递给你一些资料，并且询问你要不要加入站台的活动，主要推荐的活动有两个：
        1. 走出去计划！STEPOUT：对糖尿病说不从走路开始（Walk to stop diabetes）
    </p>
    <img src="../images/expo/image012.jpg">
    <p>走出去计划！STEPOUT<p>
    <p>走出去计划是美国糖尿病协会举办的针对糖尿病人的公益活动。目的是通过举办一起散步的活动让大家意识到运动重要性，让普通大众和糖尿病患者有一个科学阳光健康的心态面对糖尿病这个疾病，并且募集资金去帮助已经被糖尿病影响的人们。这些走出去计划的场地通常是在公园和河边等等风景美丽的地方。</p>
    <p>        一旦注册了走出去计划，管理员会给注册者一个网页，并且提供很多的网站建设方面的帮助。如果你是一个普通人愿意帮助糖尿病病人，他们教会你如何去募集资金帮助糖尿病人。如果注册者本身就是一个糖尿病人，他们会给注册者一个很有趣的名字，叫做红色神行者（Red Strider），他们会帮助这些注册者讲述自己的与糖尿病抗争的故事，通过这些故事来募集更多的资金来帮助更多的糖尿病患者。
    </p>
