    <p>2. 治愈之旅！TourdeCure：路很多，目标只有一个（Many roads, one purpose）</p>
    <p> 在1991年，美国糖尿病联盟决定利用骑车时的刺激与快乐为糖尿病人募集善款，治愈之旅（TourdeCure）应运而生。在之后的25年内，这项公益活动的参与者已经为糖尿病人募集了2亿5千万美金。这些钱很好的帮助协会履行了宗旨：预防和治疗糖尿病，并且改善所有被糖尿病影响的人的生活质量。
    </p>
    <img src="../images/expo/image020.jpg">
    <p>酷酷的志愿者--积极参加治愈之旅（TourdeCure）的糖友</p>
    <p>这名糖友参与这项运动很久了，他深切的懂得运动对于他控制他血糖的重要性。他现在以志愿者的身份参加了本次展会，并且义务帮助协会募集资金。他和我们说，运动让他更加积极健康的面对人生。我们之后也会将采访这位阳光帅气大叔的视频，大家可以继续关注后续报道。
    </p>
