    <h2 style="text-align: center;">美国糖尿病博览会</h2>
    <p>报道：郭威，沙林楠，曹雨岑</p>
    <p>图片及视频：曹雨岑</p>
    <p>美国糖尿病博览会是美国糖尿病联盟American Diabetes Association(ADA)主办的。2016年有四期博览会，本次为大家带来的报道，图片和视频均来自于的是4月30日刚刚在纽约Jacob K. Javits Convention Center闭幕的博览会。
    </p>
    <p>转载请注明出处和作者。</p>

    <p>本次博览会是全免费的，主要展示出世界最新的糖尿病防治技术和理念。本次博览会包括健康状况免费监测，糖尿病餐的制作，最新的控糖产品和服务提供商，以及顶尖糖尿病专家带来的有关于糖尿病的最新治疗报告、研究和预防手段。获得预防和控制糖尿病的最新治疗信息，了解糖尿病可怕的并发症，从而保证您和您的家庭成员的健康。</p>
    <p>本次的主题是活出健康，动出活力，改变你自己和家庭的糖尿病状况（Live healthy, be active and change the future of diabetes for you and your family）！</p>
    <p>本次展会时间是四月末，阳光明媚。展会从上午10点开始，我们一行五人去的时候已经是下午一点。本次展厅Jacob K. Javits Convention Center是一个位于纽约曼哈顿西边，紧邻美丽的哈德逊河的大型专业展厅。占地面积超过17万平方米。充满现代感的展厅和明媚的阳光以及美丽的风景也充分体现了展会的主旋律：活出健康，动出活力。这里不得不谈一种文化问题，在场地内，我们接触了很多糖尿病患者，他们与普通人一样乐观积极。并没有有丁点的不快。据调查，包括糖尿病前期的病人，美国已经有超过百分之五十的人被糖尿病所影响。这样的朝阳心理，与整个社会对于糖尿病的完善科普和包容态度，以及病人自己的阳光心态分不开。我们也对其中的一些人做了采访，请跟踪后续报道。</p>
